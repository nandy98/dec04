package javaBasics;

public class FinalConcept {

	public static void main(String[] args) {

		// final is used to restrict the variable value change
		// prevent inheritance. i.e child class can't inherit parent class when parent class is declared as final
		// prevent method overriding . i.e parent class method cannot be overridden in child class
		final int i=10;

	}

}
