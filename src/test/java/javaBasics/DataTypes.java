package javaBasics;

public class DataTypes {

	// main method is starting point of the program
	public static void main(String args[]) {
		
		//int, double, char, boolean are primitive dataType 
		//1. int
		int i = 10;
		System.out.println(i);
		//2. double
		double d =20.00;
		System.out.println(d);
		
		//3. char
		char c = 'a';
		char e = '1';
		System.out.println(c);
		//4. boolean
		boolean b1 = true;
		System.out.println(b1);
		//String: is a class, not a dataType		
		String s = "Selenium";
		String str = "123665";
		System.out.println(str);
		
		
		
	}
}
