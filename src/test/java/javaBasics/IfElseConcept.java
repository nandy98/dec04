package javaBasics;

public class IfElseConcept {

	public static void main(String[] args) {
		// Comparison operator < > <= >= == !=
		
		int a = 20;
		int b = 10;
		
		if(a>b) {
			System.out.println("a is greater than b");
		}else if (a==b){
			System.out.println("a is equal to b" );
		}else{
			System.out.println("b is greater than a");
		}

	}

}
